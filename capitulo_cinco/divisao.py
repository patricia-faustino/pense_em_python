minutes = 105

# divisão por piso: devolve somente a parte inteira do número
hours = minutes // 60
print(hours)

# modulo: divide dois números e devolve o resto
remainder = minutes % 60
print(remainder)

# x % 10: devolve o número mais a direita de x
vinte = 20.50
print(vinte % 10)

# x % 100: devolve o número mais a esquerda de x
vinte = 20.50
print(vinte % 100)